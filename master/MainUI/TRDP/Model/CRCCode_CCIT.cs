        /// <summary>  
        /// 计算给定长度数据的16位CRC  
        /// </summary>  
        /// <param name="data">要计算CRC的字节数组</param>  
        /// <returns>CRC值</returns>  
        public static UInt16 GetCrc16(Byte[] data, int start, int end)
        {   // 初始化  
            Int32 High = 0xff;  // 高字节  
            Int32 Low = 0xff;   // 低字节
            byte[] barr = new byte[end - start + 1];

            for (int i = 0; i < barr.Length;i++ )
            {
                barr[i] = data[start + i];
            }

            if (barr != null)
            {

                foreach (Byte b in barr)
                {
                    Int32 Index = Low ^ b;
                    Low = High ^ CRC16TABLE_LO[Index];
                    High = CRC16TABLE_HI[Index];
                }
            }
            return (UInt16)(~((High << 8) + Low));    // 取反  
        }



        /// <summary>  
        /// 检查给定长度数据的16位CRC是否正确  
        /// </summary>  
        /// <param name="data">要校验的字节数组</param>  
        /// <returns>  
        ///     true：正确  
        ///     false：错误  
        /// </returns>  
        /// <reamrks>  
        /// 字节数组最后2个字节为校验码，且低字节在前面，高字节在后面  
        /// </reamrks>  
        public static Boolean IsCrc16Good(Byte[] data)
        {
            // 初始化  
            Int32 High = 0xFF;
            Int32 Low = 0xFF;
            if (data != null)
            {
                foreach (Byte b in data)
                {
                    Int32 Index = Low ^ b;
                    Low = High ^ CRC16TABLE_LO[Index];
                    High = CRC16TABLE_HI[Index];
                }
            }

            return (High == 0xF0 && Low == 0xB8);
        }

