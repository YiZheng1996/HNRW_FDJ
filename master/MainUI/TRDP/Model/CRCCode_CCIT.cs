        public static ushort getCRC_CCITT(byte[] data, int startindex, int endindex)
        {

            byte hbit = 0;
            ushort crc = 0x0000;
            for (int i = startindex; i < endindex; i++)
            {
                hbit = (byte)((crc & 0xff00) >> 8);

                crc <<= 8;
                crc ^= crc_table[hbit ^ data[i]];
            }
            crc = (ushort)~crc;
            return crc;
        }

        /// <summary>
        /// 最后不取反
        /// </summary>
        /// <param name="data"></param>
        /// <param name="startindex"></param>
        /// <param name="endindex"></param>
        /// <returns></returns>
        public static ushort getCRC_NEW(byte[] data, int startindex, int endindex)
        {
            byte hbit = 0;
            ushort crc = 0x0000;
            for (int i = startindex; i < endindex; i++)
            {
                hbit = (byte)((crc & 0xff00) >> 8);
                crc <<= 8;
                crc ^= crc_table[hbit ^ data[i]];
            }
      
            return crc;
        }
